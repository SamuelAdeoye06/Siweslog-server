const PDFDocument = require('pdfkit')
const Student = require('../models/student.model')
const Placement = require('../models/placement.model')
const WeeklyLog = require('../models/weeklyLog.model')
const School = require('../models/school.model')

const COLORS = {
  navy: '#0F172A',
  blue: '#2D6BE4',
  gray: '#64748B',
  lightGray: '#E2E8F0',
  bgGray: '#F8FAFC'
}

// Helper: draw a section heading
function sectionHeading(doc, text, y) {
  doc.fontSize(11).fillColor(COLORS.blue).font('Helvetica-Bold')
  doc.text(text.toUpperCase(), 50, y, { characterSpacing: 0.5 })
  doc.moveTo(50, y + 16).lineTo(545, y + 16).strokeColor(COLORS.lightGray).lineWidth(1).stroke()
  return y + 26
}

// Helper: label + value pair
function labelValue(doc, label, value, x, y, width) {
  doc.fontSize(8).fillColor(COLORS.gray).font('Helvetica-Bold')
  doc.text(label.toUpperCase(), x, y, { characterSpacing: 0.3 })
  doc.fontSize(10).fillColor(COLORS.navy).font('Helvetica')
  doc.text(value || '—', x, y + 12, { width })
}

// @desc    Generate full logbook PDF for ITF submission
// @route   GET /api/pdf/logbook
// @access  student
const generateLogbookPDF = async (req, res) => {
  try {
    const student = await Student.findOne({ userId: req.user._id }).populate('userId', 'firstName lastName email')
    if (!student) return res.status(404).json({ message: 'Student profile not found' })

    const school = await School.findById(req.user.schoolId)
    const placement = await Placement.findOne({ studentId: student._id })
    const logs = await WeeklyLog.find({ studentId: student._id }).sort({ weekNumber: 1 })

    const approvedLogs = logs.filter(l => l.status === 'industry_approved' || l.status === 'school_approved')

    if (approvedLogs.length === 0) {
      return res.status(400).json({ message: 'You have no approved log entries yet. Get at least one week approved before exporting.' })
    }

    const doc = new PDFDocument({ size: 'A4', margin: 50, bufferPages: true })

    res.setHeader('Content-Type', 'application/pdf')
    res.setHeader('Content-Disposition', `attachment; filename="SIWES_Logbook_${student.matricNumber}.pdf"`)
    doc.pipe(res)

    // ── COVER PAGE ──
    doc.rect(0, 0, doc.page.width, 140).fill(COLORS.navy)
    doc.fontSize(22).fillColor('#FFFFFF').font('Helvetica-Bold')
    doc.text('SIWESlog', 50, 50)
    doc.fontSize(11).fillColor('#9CC3FF').font('Helvetica')
    doc.text('Students Industrial Work Experience Scheme — Digital Logbook', 50, 80)

    doc.fillColor(COLORS.navy)
    doc.fontSize(20).font('Helvetica-Bold')
    doc.text('Industrial Training Logbook', 50, 180)
    doc.fontSize(11).fillColor(COLORS.gray).font('Helvetica')
    doc.text('Final report for submission to the Industrial Training Fund (ITF)', 50, 208)

    let y = 250
    doc.roundedRect(50, y, 495, 200, 6).fillColor(COLORS.bgGray).fill()
    y += 20

    const studentName = `${student.userId.firstName} ${student.userId.lastName}`
    labelValue(doc, 'Student Name', studentName, 70, y, 220)
    labelValue(doc, 'Matric Number', student.matricNumber, 320, y, 200)
    y += 40
    labelValue(doc, 'Department', student.department, 70, y, 220)
    labelValue(doc, 'Faculty', student.faculty, 320, y, 200)
    y += 40
    labelValue(doc, 'Institution', school?.name || '—', 70, y, 220)
    labelValue(doc, 'SIWES Cycle', student.siwesCycleYear, 320, y, 200)
    y += 40
    labelValue(doc, 'Placement Company', placement?.companyName || '—', 70, y, 220)
    labelValue(doc, 'Company Location', placement?.companyLocation || '—', 320, y, 200)
    y += 40
    labelValue(doc, 'Weeks Approved', `${approvedLogs.length} of ${student.siwesDurationWeeks || 24} weeks`, 70, y, 220)
    labelValue(doc, 'Generated On', new Date().toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' }), 320, y, 200)

    doc.fontSize(8).fillColor(COLORS.gray).font('Helvetica')
    doc.text('This document was generated by SIWESlog and reflects entries approved by the assigned industry and school supervisors.', 50, 480, { width: 495, align: 'center' })

    // ── ONE PAGE PER APPROVED WEEK ──
    const dayLabels = [
      { key: 'monday', label: 'Monday' },
      { key: 'tuesday', label: 'Tuesday' },
      { key: 'wednesday', label: 'Wednesday' },
      { key: 'thursday', label: 'Thursday' },
      { key: 'friday', label: 'Friday' },
    ]

    approvedLogs.forEach((log) => {
      doc.addPage()

      // Header bar
      doc.rect(0, 0, doc.page.width, 70).fill(COLORS.navy)
      doc.fontSize(16).fillColor('#FFFFFF').font('Helvetica-Bold')
      doc.text(`Week ${log.weekNumber}`, 50, 22)
      doc.fontSize(9).fillColor('#9CC3FF').font('Helvetica')
      doc.text(
        `${new Date(log.dateFrom).toLocaleDateString('en-GB')}  –  ${new Date(log.dateTo).toLocaleDateString('en-GB')}`,
        50, 44
      )

      let cy = 95

      if (log.sectionDepartment) {
        labelValue(doc, 'Section / Department', log.sectionDepartment, 50, cy, 495)
        cy += 36
      }

      cy = sectionHeading(doc, 'Daily Record of Activities', cy)

      const filledDays = dayLabels.filter(d => log.dailyActivities?.[d.key]?.trim())
      if (filledDays.length === 0) {
        doc.fontSize(9).fillColor(COLORS.gray).font('Helvetica-Oblique')
        doc.text('No daily activities recorded', 50, cy)
        cy += 20
      } else {
        filledDays.forEach(d => {
          doc.fontSize(9).fillColor(COLORS.blue).font('Helvetica-Bold')
          doc.text(d.label, 50, cy)
          cy += 13
          doc.fontSize(9.5).fillColor(COLORS.navy).font('Helvetica')
          const textHeight = doc.heightOfString(log.dailyActivities[d.key], { width: 495 })
          doc.text(log.dailyActivities[d.key], 50, cy, { width: 495 })
          cy += textHeight + 12

          // page break guard
          if (cy > 700) {
            doc.addPage()
            cy = 50
          }
        })
      }

      if (log.weeklySummary) {
        cy = sectionHeading(doc, 'Summary of Work for the Week', cy)
        doc.fontSize(9.5).fillColor(COLORS.navy).font('Helvetica')
        const h = doc.heightOfString(log.weeklySummary, { width: 495 })
        doc.text(log.weeklySummary, 50, cy, { width: 495 })
        cy += h + 16
      }

      if (cy > 650) { doc.addPage(); cy = 50 }

      if (log.detailedReport) {
        cy = sectionHeading(doc, 'Detailed Report on Work Done', cy)
        doc.fontSize(9.5).fillColor(COLORS.navy).font('Helvetica')
        const h = doc.heightOfString(log.detailedReport, { width: 495 })
        doc.text(log.detailedReport, 50, cy, { width: 495 })
        cy += h + 16
      }

      if (cy > 600) { doc.addPage(); cy = 50 }

      // Signatures block
      cy = sectionHeading(doc, 'Approval & Sign-Off', cy)
      doc.roundedRect(50, cy, 495, 90, 4).strokeColor(COLORS.lightGray).lineWidth(1).stroke()

      labelValue(doc, 'Approved by (Industry Supervisor)', log.industrySupervisorName || '—', 65, cy + 14, 220)
      labelValue(doc, 'Date Approved', log.industrySupervisorSignedAt ? new Date(log.industrySupervisorSignedAt).toLocaleDateString('en-GB') : '—', 320, cy + 14, 200)

      if (log.industrySupervisorComment) {
        doc.fontSize(8).fillColor(COLORS.gray).font('Helvetica-Bold')
        doc.text('SUPERVISOR COMMENT', 65, cy + 46)
        doc.fontSize(9).fillColor(COLORS.navy).font('Helvetica-Oblique')
        doc.text(log.industrySupervisorComment, 65, cy + 58, { width: 465 })
      }
    })

    // ── PAGE NUMBERS ──
    const range = doc.bufferedPageRange()
    for (let i = 0; i < range.count; i++) {
      doc.switchToPage(i)
      doc.fontSize(8).fillColor(COLORS.gray).font('Helvetica')
      doc.text(
        `${i + 1} / ${range.count}`,
        50, doc.page.height - 40,
        { width: 495, align: 'center' }
      )
    }

    doc.end()
  } catch (error) {
    console.error('PDF generation error:', error)
    if (!res.headersSent) {
      res.status(500).json({ message: 'Failed to generate PDF', error: error.message })
    }
  }
}

module.exports = { generateLogbookPDF }
